class WavTrans(nn.Module):
    r""" based on SwinIR.

    Args:
        img_size (int | tuple(int)): Input image size. Default 64
        patch_size (int | tuple(int)): Patch size. Default: 1
        in_chans (int): Number of input image channels. Default: 3
        embed_dim (int): Patch embedding dimension. Default: 96
        depths (tuple(int)): Depth of each Swin Transformer layer.
        num_heads (tuple(int)): Number of attention heads in different layers.
        window_size (int): Window size. Default: 7
        mlp_ratio (float): Ratio of mlp hidden dim to embedding dim. Default: 4
        qkv_bias (bool): If True, add a learnable bias to query, key, value. Default: True
        qk_scale (float): Override default qk scale of head_dim ** -0.5 if set. Default: None
        drop_rate (float): Dropout rate. Default: 0
        attn_drop_rate (float): Attention dropout rate. Default: 0
        drop_path_rate (float): Stochastic depth rate. Default: 0.1
        norm_layer (nn.Module): Normalization layer. Default: nn.LayerNorm.
        ape (bool): If True, add absolute position embedding to the patch embedding. Default: False
        patch_norm (bool): If True, add normalization after patch embedding. Default: True
        use_checkpoint (bool): Whether to use checkpointing to save memory. Default: False
        upscale: Upscale factor. 2/3/4/8 for image SR, 1 for denoising and compress artifact reduction
        img_range: Image range. 1. or 255.
        resi_connection: The convolutional block before residual connection. '1conv'/'3conv'
    """

    def __init__(self, img_size=64, patch_size=1, in_chans=2,
                 embed_dim=96, depths=[6, 6, 6, 6], num_heads=[6, 6, 6, 6],
                 window_size=7, mlp_ratio=4., qkv_bias=True, qk_scale=None,
                 drop_rate=0., attn_drop_rate=0., drop_path_rate=0.1,
                 norm_layer=nn.LayerNorm, ape=False, patch_norm=True,
                 use_checkpoint=False, upscale=2, img_range=1., resi_connection='1conv',
                 **kwargs):
        super(WavTrans, self).__init__()
        num_in_ch = in_chans
        num_out_ch = in_chans
        self.img_range = img_range
        if in_chans == 3:
            rgb_mean = (0.4488, 0.4371, 0.4040)
            self.mean = torch.Tensor(rgb_mean).view(1, 3, 1, 1)
        else:
            self.mean = torch.zeros(1, 1, 1, 1)
        self.upscale = upscale
        self.window_size = window_size
        #---------------------------
        n_blks_dec = [2, 2, 2, 12, 8, 4]
        self.decoder = Decoder(embed_dim, num_in_ch, n_blks=n_blks_dec,upscale=self.upscale)
        self.lr_block_size = 8
        self.ref_down_block_size = 1.5
        self.dilations = [1, 2, 3]
        self.num_nbr = 1
        self.psize = 3

        #####################################################################################################
        ################################### 1, shallow feature extraction ###################################
        self.conv_first = nn.Conv2d(num_in_ch, embed_dim, 3, 1, 1)
        self.conv_pro = nn.Conv2d(embed_dim * 2, embed_dim, 3, 1, 1)
        self.conv_second = nn.Conv2d(embed_dim, embed_dim, 3, 2, 1)
        self.conv_third = nn.Conv2d(embed_dim, embed_dim, 3, 2, 1)

        self.conv_first_l1 = nn.Conv2d(num_in_ch * 3, embed_dim, 3, 1, 1)

        #####################################################################################################
        ################################### 2, deep feature extraction ######################################
        self.num_layers = len(depths)
        self.embed_dim = embed_dim
        self.ape = ape
        self.patch_norm = patch_norm
        self.num_features = embed_dim
        self.mlp_ratio = mlp_ratio

        # split image into non-overlapping patches
        self.patch_embed = PatchEmbed(
            img_size=img_size, patch_size=patch_size, in_chans=embed_dim, embed_dim=embed_dim,
            norm_layer=norm_layer if self.patch_norm else None)
        num_patches = self.patch_embed.num_patches
        patches_resolution = self.patch_embed.patches_resolution
        self.patches_resolution = patches_resolution

        # merge non-overlapping patches into image
        self.patch_unembed = PatchUnEmbed(
            img_size=img_size, patch_size=patch_size, in_chans=embed_dim, embed_dim=embed_dim,
            norm_layer=norm_layer if self.patch_norm else None)

        # absolute position embedding
        if self.ape:
            self.absolute_pos_embed = nn.Parameter(torch.zeros(1, num_patches, embed_dim))
            trunc_normal_(self.absolute_pos_embed, std=.02)

        self.pos_drop = nn.Dropout(p=drop_rate)

        # stochastic depth
        dpr = [x.item() for x in torch.linspace(0, drop_path_rate, sum(depths))]  # stochastic depth decay rule

        # build Residual Swin Transformer blocks (RSTB)
        self.layers = nn.ModuleList()
        for i_layer in range(self.num_layers):
            layer = RSTB(dim=embed_dim,
                         input_resolution=(patches_resolution[0],
                                           patches_resolution[1]),
                         depth=depths[i_layer],
                         num_heads=num_heads[i_layer],
                         window_size=window_size,
                         mlp_ratio=self.mlp_ratio,
                         qkv_bias=qkv_bias, qk_scale=qk_scale,
                         drop=drop_rate, attn_drop=attn_drop_rate,
                         drop_path=dpr[sum(depths[:i_layer]):sum(depths[:i_layer + 1])],  # no impact on SR results
                         norm_layer=norm_layer,
                         downsample=None,
                         use_checkpoint=use_checkpoint,
                         img_size=img_size,
                         patch_size=patch_size,
                         resi_connection=resi_connection

                         )
            self.layers.append(layer)
        self.norm = norm_layer(self.num_features)

        # build the last conv layer in deep feature extraction
        if resi_connection == '1conv':
            self.conv_after_body = nn.Conv2d(embed_dim, embed_dim, 3, 1, 1)
        elif resi_connection == '3conv':
            # to save parameters and memory
            self.conv_after_body = nn.Sequential(nn.Conv2d(embed_dim, embed_dim // 4, 3, 1, 1),
                                                 nn.LeakyReLU(negative_slope=0.2, inplace=True),
                                                 nn.Conv2d(embed_dim // 4, embed_dim // 4, 1, 1, 0),
                                                 nn.LeakyReLU(negative_slope=0.2, inplace=True),
                                                 nn.Conv2d(embed_dim // 4, embed_dim, 3, 1, 1))

        #####################################################################################################
        ################################ 3, high quality image reconstruction ################################

            # for lightweight SR (to save parameters)
        self.upsample = UpsampleOneStep(1, embed_dim, num_out_ch,
                                        (patches_resolution[0], patches_resolution[1]))

            # for image denoising and JPEG compression artifact reduction
        self.conv_last = nn.Conv2d(embed_dim, num_out_ch, 3, 1, 1)

        self.apply(self._init_weights)

    def _init_weights(self, m):
        if isinstance(m, nn.Linear):
            trunc_normal_(m.weight, std=.02)
            if isinstance(m, nn.Linear) and m.bias is not None:
                nn.init.constant_(m.bias, 0)
        elif isinstance(m, nn.LayerNorm):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)

    @torch.jit.ignore
    def no_weight_decay(self):
        return {'absolute_pos_embed'}

    @torch.jit.ignore
    def no_weight_decay_keywords(self):
        return {'relative_position_bias_table'}

    def check_image_size(self, x):
        _, _, h, w = x.size()
        mod_pad_h = (self.window_size - h % self.window_size) % self.window_size
        mod_pad_w = (self.window_size - w % self.window_size) % self.window_size
        x = F.pad(x, (0, mod_pad_w, 0, mod_pad_h), 'reflect')
        return x
    #=========================
    def bis(self, input, dim, index):
        # batch index select
        # input: [N, C*k*k, H*W]
        # dim: scalar > 0
        # index: [N, Hi, Wi]
        views = [input.size(0)] + [1 if i != dim else -1 for i in range(1, len(input.size()))]  # views = [N, 1, -1]
        expanse = list(input.size())
        expanse[0] = -1
        expanse[dim] = -1  # expanse = [-1, C*k*k, -1]
        index = index.clone().view(views).expand(expanse)  # [N, Hi, Wi] -> [N, 1, Hi*Wi] - > [N, C*k*k, Hi*Wi]
        return torch.gather(input, dim, index)  # [N, C*k*k, Hi*Wi]

    def forward_features_x(self, x, T1):
        x_size = (x.shape[2], x.shape[3])
        x = self.patch_embed(x)
        T1 = self.patch_embed(T1)
        if self.ape:
            x = x + self.absolute_pos_embed
            T1 = T1 + self.absolute_pos_embed
        x = self.pos_drop(x)
        T1 = self.pos_drop(T1)
        for layer in self.layers:
            x = layer(x,T1,x_size)

        x = self.norm(x)  # B L C
        x = self.patch_unembed(x, x_size)

        return x

    def forward_features_T1(self, T1, x):
        T1_size = (T1.shape[2], T1.shape[3])
        T1 = self.patch_embed(T1)
        x = self.patch_embed(x)
        if self.ape:
            T1 = T1 + self.absolute_pos_embed
            x = x + self.absolute_pos_embed
        T1 = self.pos_drop(T1)
        x = self.pos_drop(x)
        for layer in self.layers:
            T1 = layer(T1,x, T1_size)

        T1 = self.norm(T1)  # B L C
        T1 = self.patch_unembed(T1, T1_size)

        return T1

    def forward(self, x,x_T1):
        bs, _, h, w = x.size()

        # for lightweight SR
        if self.upscale == 2:
            fea_lr_l = self.conv_first(x) #60 128 128
            # ================wv l0 256 [n,2,256,256] to [n,60,256,256]
            res_fea_T1_l0 = self.conv_first(x_T1)  # [256,256]
            # ================wv l1 256 to 128
            x_T1_n = x_T1.cpu().detach().numpy()
            T1_l1 = pywt.dwt2(x_T1_n, 'haar')
            LL_l1, (LH_l1, HL_l1, HH_l1) = T1_l1
            LH_HL_l1 = np.concatenate([LH_l1, HL_l1], axis=1)  # [b,4,w,h]
            LH_HL_HH_l1 = np.concatenate([LH_HL_l1, HH_l1], axis=1)  # [b,6,w,h]
            LL_l1_t = torch.from_numpy(LL_l1).cuda()
            LH_HL_HH_l1 = torch.from_numpy(LH_HL_HH_l1).cuda()

            res_fea_LL_l1 = self.conv_first(LL_l1_t)  # [128,128]
            fea_LHH_l1 = self.conv_first_l1(LH_HL_HH_l1)  # [128,128]
            # =====128 level
            fea_lr_l1 = self.conv_after_body(self.forward_features_x(fea_lr_l, fea_LHH_l1)) + fea_lr_l
            fea_LHH_l1 = self.conv_after_body(self.forward_features_T1(fea_LHH_l1, fea_lr_l)) + fea_LHH_l1

            fea_lr_l=[fea_lr_l1]
            fea_ref_l = [fea_LHH_l1, res_fea_LL_l1, res_fea_T1_l0]  # [0,1;0,1,2]

            out = self.decoder(fea_lr_l, fea_ref_l)

        elif self.upscale == 4:
            fea_lr_l = self.conv_first(x)
            #================wv l0 256 [n,2,256,256] to [n,60,256,256]
            res_fea_T1_l0 = self.conv_first(x_T1)  #[256,256]
            #================wv l1 256 to 128
            x_T1_n = x_T1.cpu().detach().numpy()
            T1_l1 = pywt.dwt2(x_T1_n, 'haar')
            LL_l1, (LH_l1, HL_l1, HH_l1) = T1_l1
            LH_HL_l1 = np.concatenate([LH_l1, HL_l1], axis=1) #[b,4,w,h]
            LH_HL_HH_l1 = np.concatenate([LH_HL_l1, HH_l1], axis=1) #[b,6,w,h]
            LL_l1_t = torch.from_numpy(LL_l1).cuda()
            LH_HL_HH_l1 = torch.from_numpy(LH_HL_HH_l1).cuda()

            res_fea_LL_l1 = self.conv_first(LL_l1_t)  #[128,128]
            fea_LHH_l1 = self.conv_first_l1(LH_HL_HH_l1)  #[128,128]
            # ================wv l1 128 to 64
            T1_l2 = pywt.dwt2(LL_l1, 'haar')
            LL_l2, (LH_l2, HL_l2, HH_l2) = T1_l2
            LH_HL_l2 = np.concatenate([LH_l2, HL_l2], axis=1)  # [b,4,w,h]
            LH_HL_HH_l2 = np.concatenate([LH_HL_l2, HH_l2], axis=1)  # [b,6,w,h]
            LL_l2_t = torch.from_numpy(LL_l2).cuda()
            LH_HL_HH_l2 = torch.from_numpy(LH_HL_HH_l2).cuda()
            res_fea_LL_l2 = self.conv_first(LL_l2_t)  # [64,64]
            fea_LHH_l2 = self.conv_first_l1(LH_HL_HH_l2)  # [64,64]
            #=================================================================
            #=====64 level
            fea_lr_l_l2 = self.conv_after_body(self.forward_features_x(fea_lr_l,fea_LHH_l2)) + fea_lr_l
            fea_LHH_l2 = self.conv_after_body(self.forward_features_T1(fea_LHH_l2,fea_lr_l)) + fea_LHH_l2
            fea_lr_hr_cat = self.conv_pro(torch.cat([fea_lr_l_l2, fea_LHH_l2], dim=1))
            fea_lr_hr_cat = self.conv_pro(torch.cat([fea_lr_hr_cat, res_fea_LL_l2], dim=1))
            fea_lr_l_l1 = F.interpolate(fea_lr_hr_cat, scale_factor=2, mode='bilinear', align_corners=False)
            #=====128 level
            fea_lr_l1 = self.conv_after_body(self.forward_features_x(fea_lr_l_l1,fea_LHH_l1)) + fea_lr_l_l1
            fea_LHH_l1 = self.conv_after_body(self.forward_features_T1(fea_LHH_l1,fea_lr_l_l1)) + fea_LHH_l1

            fea_lr_l = [fea_lr_l1]#[0,1]
            fea_ref_l = [fea_LHH_l1, res_fea_LL_l1,res_fea_T1_l0]#[0,1;0,1,2]

            out = self.decoder(fea_lr_l, fea_ref_l)

        return out

    def flops(self):
        flops = 0
        H, W = self.patches_resolution
        flops += H * W * 3 * self.embed_dim * 9
        flops += self.patch_embed.flops()
        for i, layer in enumerate(self.layers):
            flops += layer.flops()
        flops += H * W * 3 * self.embed_dim * self.embed_dim
        flops += self.upsample.flops()
        return flops


